package paquete;

public class Act2_Numeros1_100_Usando_While {

	public static void main(String[] args) {
		//2. Muestra los números del 1 al 100 (ambos incluidos). Usa un bucle while.
		int contador =1;
		while(contador<=100) {
			
			System.out.println(contador);
			contador++;
		}
	}

}
